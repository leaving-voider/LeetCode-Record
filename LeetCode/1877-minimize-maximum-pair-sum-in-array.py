###############################################################################################
# 排序 + 贪心 
###########
# 时间复杂度：O(nlogn)
# 空间复杂度：O(logn), 排序消耗栈空间
###############################################################################################
class Solution:
    def minPairSum(self, nums: List[int]) -> int:
        nums.sort()
        n, res = len(nums), 0
        for i in range(n//2):
            res = max(res, nums[i] + nums[n - i - 1])
        return res